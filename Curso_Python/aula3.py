"""
Python = Linguaguem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto

Strings são textos que estão dentro de aspas

"""
# Obs: Por padrão o print vai exibir o \n (quebra de linha) mesmo que vazio

# Aspas simples
print('Rafael Ricardo')
# É possível colar aspas dentro do outro tipo de aspas, exemplo:
print('Rafael "Ricardo"')
print("Rafael 'Ricardo'")

# Aspas duplas
print("Rafael Ricardo")

# Escape - O interpretador pula o proximo caracter após a \
print("Rafael \"Ricardo\"")

# r - Exibe o caractere de escape
print(r"Rafael \"Ricardo\"")
