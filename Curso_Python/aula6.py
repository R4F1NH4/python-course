# Conversão de tipos, coerção
# Type convertion, typecasting, coercion
# é o ato de converter um tipo em outros tipos imutáveis e primitivos:
# str, int, float, bool

print(1+1)
print('a'+'b') # Concatenação
print(int('1') + 1) # Coersão = Transformou o '1' que era str em int

# Obs: sempre que há uma função dentro da outra, ele executa sempre
# de dentro para fora, independente de quantas tenham.
